package org.example;

public class ArithmeticOperators {

    public static void main(String[] args){
        int A=30;
        int B=10;
        byte n=127;
        long myNum = 1200000000L;
        short num =6000;
        int i=20;
        float f1 = 12.34f;
        double d = 1233.233;
        char c = 'S';
        boolean iLikejava = true;
        boolean iLikeatesting = false;
        String s = ("Hello");
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        System.out.println(A%B);
        System.out.println(iLikejava);
        System.out.println(iLikeatesting);
        System.out.println(s);
        System.out.println(B+n);
        System.out.println(myNum-num);
        System.out.println(num*i);
        System.out.println(f1/d);
        System.out.println(num%i);
        System.out.println(++A);
        System.out.println(--A);
    }
}
